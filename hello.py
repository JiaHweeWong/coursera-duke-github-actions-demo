def add(x, y):
    """This is an add function"""

    return x + y
    
    
def multiply(x, y):
    """
    This is a multiply function
    """
    
    return x * y
    
def subtract(from_num, by_num):
    """
    This is a subtract function, to subtract from from_num by by_num
    """
    
    return from_num - by_num

def divide(from_num, by_num):
    """
    This is a divide function, to divide from_num by by_num
    """
    
    return from_num / by_num


print(add(1, 1))
print(multiply(2,3))
print(subtract(10,2))
print(divide(20,5))